    <h2 id="areapersonal" class="titulo2">Red de área personal WPAN</h2>
    <p>Personal Area Network (PAN), en español Red de Área Personal, es un estándar de red para la comunicación entre distintos dispositivos (computadoras, puntos de acceso a internet, teléfonos celulares, PDA, dispositivos de audio, impresoras) cercanos al punto de acceso.</p>
    <p>Estas redes son de unos pocos metros y para uso personal.</p>
  </div>
  
  <!-- #Contenido -->
  <div>
    <h2 id="arealocal" class="titulo2">Red de área local LAN</h2>
    <p>Es una red que cubre una extensión reducida como una empresa, una universidad, un colegio, etc. No habrá por lo general dos ordenadores que disten entre si más de un kilómetro.</>
    <p>Una configuración típica en una red de área local es tener una computadora llamada servidor de ficheros en la que se almacena todo el software de control de la red así como el software que se comparte con los demás ordenadores de la red. Los ordenadores que no son servidores de ficheros reciben el nombre de estaciones de trabajo. Estos suelen ser menos potentes y tienen software personalizado por cada usuario. La mayoría de las redes LAN están conectadas por medio de cables y tarjetas de red, una en cada equipo.</>
  </div>
  
  <!-- #Contenido -->
  <div>
    <h2 id="areametropolitana" class="titulo2">Red de área metropolitana MAN</h2>
    <p>Las redes de área metropolitana cubren extensiones mayores como pueden ser una ciudad o un distrito. Mediante la interconexión de redes LAN se distribuyen la informática a los diferentes puntos del distrito. Bibliotecas, universidades u organismos oficiales suelen interconectarse mediante este tipo de redes.</p>
  </div>
  
  <!-- #Contenido -->
  <div>
    <h2 id="areaamplia" class="titulo2">Red de área amplia WAN</h2>
    <p>Las redes de área extensa cubren grandes regiones geográficas como un país, un continente o incluso el mundo.
    Cable transoceánico o satélites se utilizan para enlazar puntos que distan grandes distancias entre si.</p>
    <p>Con el uso de una WAN se puede conectar desde España con Japón sin tener que pagar enormes cantidades de teléfono. La implementación de una red de área extensa es muy complicada. Se utilizan multiplexadores para conectar las redes metropolitanas a redes globales utilizando técnicas que permiten que redes de diferentes características pueden comunicarse sin problema.</p>
    <p><strong>El mejor ejemplo de una red de área extensa es Internet.</strong></p>
  </div>
  
  <!-- #Contenido -->
  <div>
    <h2 id="internet" class="titulo2">Internet</h2>
    <p><strong>I N T E R N E T - Es un ejemplo de una red W A N</strong></p>
    <p>Es un conjunto descentralizado de redes de comunicaciones interconectadas, que utilizan la familia de protocolos TCP/IP, lo cual garantiza que las redes físicas heterogéneas que la componen constituyen una red lógica única de alcance mundial. Sus orígenes se remontan a 1969, cuando se estableció la primera conexión de computadoras, conocida como ARPANET, entre tres universidades en California (Estados Unidos).</p>
    <p>Uno de los servicios que más éxito ha tenido en internet ha sido la World Wide Web (WWW o la Web), hasta tal punto que es habitual la confusión entre ambos términos. La WWW es un conjunto de protocolos que permite, de forma sencilla, la consulta remota de archivos de hipertexto. Esta fue un desarrollo posterior (1990) y utiliza internet como medio de transmisión.</p>
    <p>Existen, por tanto, muchos otros servicios y protocolos en internet, aparte de la Web: el envío de correo electrónico (SMTP), la transmisión de archivos (FTP y P2P), las conversaciones en línea (IRC), la mensajería instantánea y presencia, la transmisión de contenido y comunicación multimedia telefonía (VoIP), televisión (IPTV), los boletines electrónicos (NNTP), el acceso remoto a otros dispositivos (SSH y Telnet) o los juegos en línea.</p>
